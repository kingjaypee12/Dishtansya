    /**
     * Store a newly created resource in storage.
     *
     * @param  \App\Http\Requests\StoreOrderRequest  $request
     * @return \Illuminate\Http\Response
     */
    public function store(StoreOrderRequest $request)
    {
        //get the product collection from the database
        $product = Product::find($request->product_id);

        //check if product is less than the quantity if true return invalid order else continue with placing order
        if ($product->stock < $request->quantity) {
            return response()->json(['message' => 'Failed to order this product due to unavailability of the stock'], 400);
        }

        //place the order
        Order::create([
            'quantity' => $request->quantity,
            'user_id' => Auth::user()->id,
            'product_id' => $request->product_id
        ]);
        
        //once the order has been successfully placed, deduct the given quantity to available stock
        $product = Product::find($request->product_id);
        $product->stock -= $request->quantity;
        $product->save();

        return response()->json(['message' => 'You have successfully ordered this product'], 201);
    }

